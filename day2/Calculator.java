package week1.day2;

public class Calculator {
	public int addInt(int N1,int N2,int N3 ) {

		int sum = N1+N2+N3;
		return sum;
	}

		public int subInt(int N1,int N2){
			int sub = N1 - N2;
			return sub;
		}

		public double mulnum(double N1, double N2) {

		double mul = N1*N2;
		return mul;
		}

	public float divNum(float N1,float N2) {
		float div = N1/N2;
		return div;
	}
}
